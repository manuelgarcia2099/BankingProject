public class TbUsuario {
    private Integer code_use;

    private String name_use;

    private String sobr_use;

    private String emai_use;

    private String pass_use;

    public Integer getCode_use() {
        return code_use;
    }

    public void setCode_use(Integer code_use) {
        this.code_use = code_use;
    }

    public String getName_use() {
        return name_use;
    }

    public void setName_use(String name_use) {
        this.name_use = name_use;
    }

    public String getSobr_use() {
        return sobr_use;
    }

    public void setSobr_use(String sobr_use) {
        this.sobr_use = sobr_use;
    }

    public String getEmai_use() {
        return emai_use;
    }

    public void setEmai_use(String emai_use) {
        this.emai_use = emai_use;
    }

    public String getPass_use() {
        return pass_use;
    }

    public void setPass_use(String pass_use) {
        this.pass_use = pass_use;
    }
}